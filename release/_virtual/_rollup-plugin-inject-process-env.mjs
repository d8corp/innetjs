(function() {
    const env = {"INNETJS_INNETJS_PACKAGE_VERSION":"2.2.6"};
    try {
        if (process) {
            process.env = Object.assign({}, process.env);
            Object.assign(process.env, env);
            return;
        }
    } catch (e) {} // avoid ReferenceError: process is not defined
    globalThis.process = { env:env };
})();
